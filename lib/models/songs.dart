class Songs {
  final String? id;
  final int? trackNumber;
  final String? title;
  final String? album;
  final String? albumId;
  final String? artist;
  final String? artistId;
  final String? albumPicture;
  final bool? favourite;
  final String? length;

  Songs({
    this.id,
    this.trackNumber,
    this.title,
    this.album,
    this.albumId,
    this.artist,
    this.artistId,
    this.albumPicture,
    this.favourite,
    this.length
  });
}
